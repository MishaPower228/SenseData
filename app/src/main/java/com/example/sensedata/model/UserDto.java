package com.example.sensedata.model;

public class UserDto {
    private int id;
    private String username;
    private String email;
    private String roleName;

    public int getId() {
        return id;
    }

    public String getUsername() {
        return username;
    }

    public String getEmail() {
        return email;
    }

    public String getRoleName() {
        return roleName;
    }
}
